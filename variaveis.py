# Tipos de variáveis
# 1. int: números inteiros, ou seja, números sem parte decimal: 0, 5, -5, 1552
# 2. float: números reais, ou seja, números com parte decimal: 1.0, -3.8, 3.54
# 3. str: cadeias de caracteres, ou seja, dados textuais mais chamados de string
# 4. bool: valores lógicos (booleanos): True or False

idade = 26
# Variável do tipo int
altura = 1.88
# Variável do tipo float
nome = 'Asnor Ferreira'
# Variável do tipo str
